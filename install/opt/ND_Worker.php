class ND_Worker extends Thread {
	protected $obj = NULL;

	public function __construct($obj) {
		parent::__construct();

		/* Required methods on $obj:
		 *
		 * $obj->worker()  - A callable function that will be invoked when this thread starts.
		 *
		 */
		$this->obj = $obj;
	}

	public function run() {
		$this->obj->worker($this);
	}
}
